// WAP to pass 2 strings in a method and change their case to upper.

class FunUpperCase  
{  
    static void upper(String str1, String str2)  
    {  
        System.out.println("String 1 : " + str1.toUpperCase());  
        System.out.println("String 2 : " + str2.toUpperCase());  
    }  
    
    public static void main(String args[])  
    {   
        upper("Hello", "Aditya Raj");  
    }  
}


